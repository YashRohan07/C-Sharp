using System;

namespace Variables___Literals
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int value = 5;

            //Variable
            Console.WriteLine(value);

            //Literal
            Console.WriteLine(50.05);

            Console.ReadLine();
        }
    }
}

/*
Console.WriteLine(value)   This line outputs the value of the variable value to the console. it will print the value 5.
Console.WriteLine(50.05)   This line directly prints the literal value 50.05 to the console. A literal is a fixed value that is written directly into the source code.
*/
